self.requestAnimationFrame = function() {
 var nextRAF = 0;
 return function(func) {
  var now = Date.now();
  if (nextRAF === 0) {
   nextRAF = now + 1e3 / self.targetFps;
  } else {
   while (now + 2 >= nextRAF) {
    nextRAF += 1e3 / self.targetFps;
   }
  }
  var delay = Math.max(nextRAF - now, 0);
  return setTimeout(func, delay);
 };
}();

